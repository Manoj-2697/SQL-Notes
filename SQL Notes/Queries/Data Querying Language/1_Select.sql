-- This command is used to select columns within a table.
-- SELECT clause must always be associated with the FROM clause.
-- All the Aggregate operations, String operations, ranking operations, etc. must be performed 
-- within the SELECT clause.
-- Syntax: SELECT column1, column2, ... FROM table_name.
select iso_code from coviddeaths;

-- This command selects all the columns from the table.
select * from coviddeaths;